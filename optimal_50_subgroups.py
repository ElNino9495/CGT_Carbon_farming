@dataclass
class BundleData:
    ids: List[str]
    cats: List[str]
    ex_groups: List[str]
    CSP: List[float]
    OC: List[float]
    DY: List[float]
    alpha: Dict[Tuple[int,int], float]
    delta: Dict[Tuple[int,int], float]
    gamma: Dict[Tuple[int,int], float]
    incompat_idx: List[Tuple[int,int]]

def generate_data(practices: List[str], seed: int, out_dir: str) -> BundleData:
    rng = random.Random(seed)
    ids = practices[:]
    n = len(ids)

    cats, ex_groups = [], []
    for nm in ids:
        c, ex = classify_practice(nm)
        cats.append(c)
        ex_groups.append(ex)

    CSP, OC, DY = [], [], []
    for i in range(n):
        r = BASE_RANGES.get(cats[i], BASE_RANGES["general"])
        csp = tri(rng, *r["CSP"], mode_frac=0.62)
        dy  = tri(rng, *r["DY"],  mode_frac=0.55)
        oc  = tri(rng, *r["OC"],  mode_frac=0.58)

        s = ids[i].lower()

        if cats[i] == "water":
            if any(k in s for k in ["drain", "awd", "aerobic"]):
                dy += rng.uniform(-50, 30)
            if "laser" in s:
                oc += rng.uniform(1200, 2200)
                dy += rng.uniform(20, 80)

        if cats[i] == "nitrogen":
            if any(k in s for k in ["inhibitor", "controlled-release", "cru", "udp", "deep placement"]):
                oc += rng.uniform(900, 2200)
                dy += rng.uniform(0, 40)
            if any(k in s for k in ["lcc", "ssnm", "precision"]):
                dy += rng.uniform(10, 60)

        if cats[i] == "residue":
            if "no open-field burning" in s:
                oc += rng.uniform(200, 800)
            if "incorporation" in s:
                dy += rng.uniform(-40, 40)
            if "compost" in s:
                dy += rng.uniform(10, 60)

        if cats[i] == "soil":
            if "biochar" in s:
                oc += rng.uniform(1500, 3500)
                csp += rng.uniform(0.3, 0.9)
                dy += rng.uniform(0, 60)
            if "cover crop" in s or "green manuring" in s:
                oc += rng.uniform(500, 1400)
                csp += rng.uniform(0.2, 0.6)

        csp = max(0.02, min(3.5, csp))
        dy  = max(-400.0, min(600.0, dy))
        oc  = max(150.0, min(9000.0, oc))

        CSP.append(round(csp, 4))
        DY.append(round(dy, 2))
        OC.append(round(oc, 2))

    complementary = {
        ("water", "nitrogen"), ("water", "residue"), ("water", "soil"),
        ("nitrogen", "soil"), ("residue", "soil"), ("establishment", "water"),
        ("establishment", "nitrogen"),
    }
    def is_comp(a: str, b: str) -> bool:
        return (a, b) in complementary or (b, a) in complementary

    alpha: Dict[Tuple[int,int], float] = {}
    delta: Dict[Tuple[int,int], float] = {}
    gamma: Dict[Tuple[int,int], float] = {}

    for i, j in combinations(range(n), 2):
        same = (cats[i] == cats[j])
        comp = is_comp(cats[i], cats[j])

        mu = (0.30 if comp else 0.0) + (-0.22 if same else 0.0)
        z1 = rng.gauss(mu, 0.50)
        z2 = rng.gauss(mu, 0.70)
        z3 = rng.gauss((0.18 if same else 0.0) + (-0.08 if comp else 0.0), 0.38)

        a = (0.25 * min(CSP[i], CSP[j])) * z1
        a = max(-0.80, min(1.00, a))
        alpha[(i, j)] = round(a, 4)

        base_y = min(max(abs(DY[i]), 70.0), max(abs(DY[j]), 70.0), 280.0)
        d = (0.25 * base_y) * z2
        d = max(-220.0, min(260.0, d))
        delta[(i, j)] = round(d, 2)

        g = (0.20 * min(OC[i], OC[j])) * z3
        g = max(-1000.0, min(2200.0, g))
        gamma[(i, j)] = round(g, 2)

    incompat: Set[Tuple[int,int]] = set()

    # A) extra exclusivity (optional, mostly redundant once we enforce REAL_GROUPS <=1)
    group_to_items: Dict[str, List[int]] = {}
    for idx, g in enumerate(ex_groups):
        if g:
            group_to_items.setdefault(g, []).append(idx)
    for g, items in group_to_items.items():
        if len(items) >= 2:
            for i, j in combinations(items, 2):
                incompat.add(pair_key(i, j))

    # B) extra hard-conflict pairs (small %)
    all_pairs = list(combinations(range(n), 2))
    def conflict_score(i: int, j: int) -> float:
        a = alpha[(i, j)]
        d = delta[(i, j)] / 200.0
        g = gamma[(i, j)] / 1500.0
        return a + d - g

    ranked = sorted(all_pairs, key=lambda p: conflict_score(p[0], p[1]))
    target_extra = int(0.05 * len(all_pairs))
    added = 0
    for (i, j) in ranked:
        if added >= target_extra:
            break
        if (i, j) in incompat:
            continue
        if conflict_score(i, j) < -0.60:
            incompat.add((i, j))
            added += 1

    incompat_idx = sorted(incompat)

    os.makedirs(out_dir, exist_ok=True)

    df_pr = pd.DataFrame({
        "idx": list(range(n)),
        "practice": ids,
        "category": cats,
        "exclusive_group": ex_groups,
        "CSP_tCO2e": CSP,
        "OC_INR": OC,
        "dY_kg_per_ha": DY,
    })
    df_pr.to_csv(os.path.join(out_dir, "practices.csv"), index=False)

    df_int = pd.DataFrame([
        {"i": i, "j": j, "alpha": alpha[(i, j)], "delta": delta[(i, j)], "gamma": gamma[(i, j)]}
        for (i, j) in all_pairs
    ])
    df_int.to_csv(os.path.join(out_dir, "interactions.csv"), index=False)

    df_inc = pd.DataFrame([{"i": i, "j": j} for (i, j) in incompat_idx])
    df_inc.to_csv(os.path.join(out_dir, "incompatibilities.csv"), index=False)

    print(f"[OK] Wrote: {out_dir}/practices.csv, interactions.csv, incompatibilities.csv")
    print(f"n={n}, pairs={len(all_pairs)}, incompatibilities={len(incompat_idx)}")
    print("CSP range:", (min(CSP), max(CSP)), "tCO2e")
    print("DY  range:", (min(DY), max(DY)), "kg/ha")
    print("OC  range:", (min(OC), max(OC)), "INR")

    return BundleData(
        ids=ids, cats=cats, ex_groups=ex_groups,
        CSP=CSP, OC=OC, DY=DY,
        alpha=alpha, delta=delta, gamma=gamma,
        incompat_idx=incompat_idx
    )


