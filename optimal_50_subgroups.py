# 3) Build objective coefficients
# ============================================================
def build_objective(data: BundleData, CCP: float, CROP_PRICE: float):
    n = len(data.ids)
    b = [CCP * data.CSP[i] + CROP_PRICE * data.DY[i] - data.OC[i] for i in range(n)]

    w = [[0.0] * n for _ in range(n)]
    gmat = [[0.0] * n for _ in range(n)]
    for (i, j), a in data.alpha.items():
        d = data.delta[(i, j)]
        g = data.gamma[(i, j)]
        wij = CCP * a + CROP_PRICE * d - g
        w[i][j] = w[j][i] = float(wij)
        gmat[i][j] = gmat[j][i] = float(g)
    return b, w, gmat


# ============================================================
# 4) GRASP with NEW subgroup constraint (<= 1 per REAL_GROUP)
# ============================================================
def build_incompat_sets(n: int, incompat_idx: List[Tuple[int,int]]) -> List[Set[int]]:
    inc = [set() for _ in range(n)]
    for i, j in incompat_idx:
        inc[i].add(j)
        inc[j].add(i)
    return inc

def grasp_solve(n: int, b: List[float], w: List[List[float]], OC: List[float], gmat: List[List[float]],
                incompat: List[Set[int]], B: float,
                subgroup_of: List[int], num_groups: int,
                iters: int = 200, alpha_rcl: float = 0.3, p_explore: float = 0.10, seed: int = 0):
    rng = random.Random(seed)

    def construct_once():
        inS = [False]*n
        group_taken = [False]*num_groups

        S = []
        sumW = [0.0]*n
        sumG = [0.0]*n
        cur_val = 0.0
        cur_cost = 0.0

        while True:
            cand = []
            for i in range(n):
                if inS[i]:
                    continue

                gi = subgroup_of[i]
                if group_taken[gi]:
                    continue

                if any(inS[j] for j in incompat[i]):
                    continue

                add_cost = OC[i] + sumG[i]
                if cur_cost + add_cost > B + 1e-9:
                    continue

                add_gain = b[i] + sumW[i]
                score = add_gain / max(add_cost, 1.0)
                cand.append((i, score, add_gain, add_cost))

            if not cand:
                break

            pos = [c for c in cand if c[2] > 1e-9]
            if pos:
                pool = pos
            else:
                if rng.random() > p_explore:
                    break
                pool = cand

            scores = [c[1] for c in pool]
            smax, smin = max(scores), min(scores)
            cutoff = smax - alpha_rcl*(smax - smin)
            rcl = [c for c in pool if c[1] >= cutoff]
            i = rng.choice(rcl)[0]

            # add i
            inS[i] = True
            group_taken[subgroup_of[i]] = True
            S.append(i)

            cur_val += b[i] + sumW[i]
            cur_cost += OC[i] + sumG[i]

            for k in range(n):
                if k == i:
                    continue
                sumW[k] += w[k][i]
                sumG[k] += gmat[k][i]

        return S

    def local_search(S0: List[int]):
        inS = [False]*n
        group_taken = [False]*num_groups
        for i in S0:
            inS[i] = True
            group_taken[subgroup_of[i]] = True

        sumW = [0.0]*n
        sumG = [0.0]*n
        cur_val = 0.0
        cur_cost = 0.0

        for i in range(n):
            if inS[i]:
                cur_val += b[i]
                cur_cost += OC[i]

        for i, j in combinations(range(n), 2):
            if inS[i] and inS[j]:
                cur_val += w[i][j]
                cur_cost += gmat[i][j]

        for i in range(n):
            sw = 0.0
            sg = 0.0
            for j in range(n):
                if inS[j] and j != i:
                    sw += w[i][j]
                    sg += gmat[i][j]
            sumW[i] = sw
            sumG[i] = sg

        def feasible_add(x: int) -> bool:
            if inS[x]:
                return False
            gx = subgroup_of[x]
            if group_taken[gx]:
                return False
            if any(inS[j] for j in incompat[x]):
                return False
            return cur_cost + (OC[x] + sumG[x]) <= B + 1e-9

        improved = True
        while improved:
            improved = False
            best_gain = 0.0
            best_move = None

            # ADD
            for j in range(n):
                if feasible_add(j):
                    gain = b[j] + sumW[j]
                    if gain > best_gain + 1e-9:
                        best_gain = gain
                        best_move = ("add", j)

            # REMOVE
            for i in range(n):
                if inS[i]:
                    gain = -(b[i] + sumW[i])
                    if gain > best_gain + 1e-9:
                        best_gain = gain
                        best_move = ("rem", i)

            # SWAP
            for i in range(n):
                if not inS[i]:
                    continue
                gi = subgroup_of[i]
                cost_after_rem = cur_cost - (OC[i] + sumG[i])

                for j in range(n):
                    if inS[j]:
                        continue

                    gj = subgroup_of[j]
                    # after removing i, group gi becomes free; allow adding j if:
                    # - gj is free, OR gj == gi (because i is removed)
                    if group_taken[gj] and gj != gi:
                        continue

                    # incompat check against S\{i}
                    bad = False
                    for k in incompat[j]:
                        if inS[k] and k != i:
                            bad = True
                            break
                    if bad:
                        continue

                    add_cost_after = OC[j] + (sumG[j] - gmat[j][i])
                    if cost_after_rem + add_cost_after > B + 1e-9:
                        continue

                    add_gain_after = b[j] + (sumW[j] - w[j][i])
                    rem_gain = -(b[i] + sumW[i])
                    gain = rem_gain + add_gain_after
                    if gain > best_gain + 1e-9:
                        best_gain = gain
                        best_move = ("swap", i, j)

            if best_move is None:
                break

            improved = True

            def apply_remove(i: int):
                nonlocal cur_val, cur_cost
                inS[i] = False
                group_taken[subgroup_of[i]] = False

                cur_val -= b[i] + sumW[i]
                cur_cost -= OC[i] + sumG[i]

                for k in range(n):
                    if k == i:
                        continue
                    sumW[k] -= w[k][i]
                    sumG[k] -= gmat[k][i]

                sumW[i] = 0.0
                sumG[i] = 0.0

            def apply_add(j: int):
                nonlocal cur_val, cur_cost
                inS[j] = True
                group_taken[subgroup_of[j]] = True

                cur_val += b[j] + sumW[j]
                cur_cost += OC[j] + sumG[j]

                for k in range(n):
                    if k == j:
                        continue
                    sumW[k] += w[k][j]
                    sumG[k] += gmat[k][j]

                sw = 0.0
                sg = 0.0
                for k in range(n):
                    if inS[k] and k != j:
                        sw += w[j][k]
                        sg += gmat[j][k]
                sumW[j] = sw
                sumG[j] = sg

            typ = best_move[0]
            if typ == "add":
                apply_add(best_move[1])
            elif typ == "rem":
                apply_remove(best_move[1])
            else:
                i, j = best_move[1], best_move[2]
                apply_remove(i)
                apply_add(j)

        S_final = [i for i in range(n) if inS[i]]
        return S_final, cur_val, cur_cost

    best_S, best_val, best_cost = [], float("-inf"), 0.0
    for _ in range(iters):
        S0 = construct_once()
        S1, v1, c1 = local_search(S0)
        if v1 > best_val:
            best_S, best_val, best_cost = S1, v1, c1

    return best_S, best_val, best_cost


# ============================================================
# 5) MILP warm-started from GRASP + NEW subgroup constraints
#     Also: we only create y_ij for CROSS-SUBGROUP pairs (smaller MILP)
# ============================================================
def solve_milp(n: int, b: List[float], w: List[List[float]], OC: List[float], gmat: List[List[float]],
              incompat_idx: List[Tuple[int,int]], B: float, start_S: List[int] | None,
              subgroup_of: List[int], group_names: List[str],
              time_limit: int | None = None, warmstart: bool = True):
    try:
        import pulp
    except ImportError:
        import sys, subprocess
        subprocess.check_call([sys.executable, "-m", "pip", "install", "pulp"])
        import pulp

    prob = pulp.LpProblem("QKP_Bundle_Grouped", pulp.LpMaximize)

    x = {i: pulp.LpVariable(f"x_{i}", cat="Binary") for i in range(n)}

    # only cross-group pairs matter (within group cannot both be 1)
    pairs = [(i, j) for i in range(n) for j in range(i+1, n) if subgroup_of[i] != subgroup_of[j]]
    y = {(i, j): pulp.LpVariable(f"y_{i}_{j}", cat="Binary") for (i, j) in pairs}

    # objective
    prob += pulp.lpSum(b[i] * x[i] for i in range(n)) + pulp.lpSum(w[i][j] * y[(i, j)] for (i, j) in pairs)

    # incompatibilities (still allowed; many will be redundant now)
    for (i, j) in incompat_idx:
        prob += x[i] + x[j] <= 1

    # subgroup constraints: at most one from each REAL subgroup
    # build items per group id
    G = len(group_names)
    group_items = {gid: [] for gid in range(G)}
    for i in range(n):
        group_items[subgroup_of[i]].append(i)
    for gid, items in group_items.items():
        prob += pulp.lpSum(x[i] for i in items) <= 1

    # linearize y_ij = x_i * x_j for cross-group pairs
    for (i, j) in pairs:
        prob += y[(i, j)] <= x[i]
        prob += y[(i, j)] <= x[j]
        prob += y[(i, j)] >= x[i] + x[j] - 1

    # budget
    prob += pulp.lpSum(OC[i] * x[i] for i in range(n)) + pulp.lpSum(gmat[i][j] * y[(i, j)] for (i, j) in pairs) <= B

    # warm start
    if warmstart and start_S is not None:
        Sset = set(start_S)
        for i in range(n):
            x[i].setInitialValue(1 if i in Sset else 0)
        for (i, j) in pairs:
            y[(i, j)].setInitialValue(1 if (i in Sset and j in Sset) else 0)

    solver_kwargs = {"msg": False}
    if time_limit is not None:
        solver_kwargs["timeLimit"] = time_limit
    if warmstart:
        solver_kwargs["warmStart"] = True

    solver = pulp.PULP_CBC_CMD(**solver_kwargs)
    prob.solve(solver)

    status = pulp.LpStatus[prob.status]
    chosen = [i for i in range(n) if int(pulp.value(x[i])) == 1]
    obj = float(pulp.value(prob.objective))
    return status, chosen, obj


