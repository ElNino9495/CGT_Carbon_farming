def build_subgroup_of(n: int) -> Tuple[List[int], List[str]]:
    """Returns subgroup_of[i] = group_id in [0..G-1] and group_names list."""
    group_names = list(REAL_GROUPS.keys())
    subgroup_of = [-1] * n
    for gid, gname in enumerate(group_names):
        for i in REAL_GROUPS[gname]:
            if 0 <= i < n:
                subgroup_of[i] = gid
    if any(g == -1 for g in subgroup_of):
        raise ValueError("Some practices were not assigned to a REAL_GROUP. Check index ranges.")
    return subgroup_of, group_names


