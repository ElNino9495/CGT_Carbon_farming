import math
from itertools import combinations

def bundle_components(S, CSP, DY, OC, alpha, delta, gamma):
    """
    Return (V, Y, W) for a subset S of practice indices.
    V: total carbon outcome (incl alpha)
    Y: total yield change (incl delta)
    W: total cost (incl gamma)
    """
    V = sum(CSP[i] for i in S)
    Y = sum(DY[i]  for i in S)
    W = sum(OC[i]  for i in S)

    for a, b in combinations(S, 2):
        jk = (a, b) if a < b else (b, a)
        V += alpha.get(jk, 0.0)
        Y += delta.get(jk, 0.0)
        W += gamma.get(jk, 0.0)

    return V, Y, W

def g_value(S, CCP, CROP_PRICE, CSP, DY, OC, alpha, delta, gamma):
    """
    Characteristic value for subset S (practice indices).
    """
    V, Y, W = bundle_components(S, CSP, DY, OC, alpha, delta, gamma)
    return CCP * V + CROP_PRICE * Y - W

def shapley_values(bundle, CCP, CROP_PRICE, CSP, DY, OC, alpha, delta, gamma):
    """
    Compute Shapley values for practices in 'bundle' (list of indices).
    Uses exact enumeration over all subsets of bundle (OK since bundle small).
    """
    n = len(bundle)
    if n == 0:
        return {}

    # Map local positions 0..n-1 to global practice indices
    players = list(bundle)

    # Precompute factorials for weights
    fact = [math.factorial(i) for i in range(n+1)]
    denom = fact[n]

    phi = {i: 0.0 for i in players}

    # Enumerate subsets S of players not containing i
    # We'll build subsets by size
    for i in players:
        others = [p for p in players if p != i]
        for s in range(0, n):  # size of subset from 0 to n-1
            for S in combinations(others, s):
                S = list(S)
                w = fact[s] * fact[n - s - 1] / denom
                mc = g_value(S + [i], CCP, CROP_PRICE, CSP, DY, OC, alpha, delta, gamma) - \
                     g_value(S,        CCP, CROP_PRICE, CSP, DY, OC, alpha, delta, gamma)
                phi[i] += w * mc

    return phi

