# ---- Feasibility + objective helpers ----
def total_cost(S, OC, gamma):
    """Cost = sum OC + sum gamma pair costs."""
    cost = sum(OC[i] for i in S)
    for a, b in combinations(sorted(S), 2):
        cost += gamma.get(pair_key(a, b), 0.0)
    return cost

def is_feasible_set(S, incompat_idx, B, OC, gamma):
    Sset = set(S)
    for (i, k) in incompat_idx:
        if i in Sset and k in Sset:
            return False
    return total_cost(S, OC, gamma) <= B

def obj_value(S, b_lin, w_pair_obj):
    """Net objective = linear terms + quadratic interaction terms."""
    val = sum(b_lin[i] for i in S)
    for a, b in combinations(sorted(S), 2):
        val += w_pair_obj.get(pair_key(a, b), 0.0)
    return val

# ---- GRASP: randomized greedy construction ----
def grasp_construct(m, incompat_idx, B, OC, gamma, b_lin, w_pair_obj,
                    alpha_rcl=0.3, p_explore=0.10, rng=None):
    """
    Build a feasible solution with a Greedy Randomized (RCL) rule.
    alpha_rcl in [0,1]: smaller = greedier, larger = more random.
    p_explore: if all marginal gains are <=0, occasionally add a random feasible item to explore synergies.
    """
    if rng is None:
        rng = random.Random()

    S = []
    base_obj = obj_value(S, b_lin, w_pair_obj)
    base_cost = total_cost(S, OC, gamma)

    while True:
        candidates = []
        for i in range(m):
            if i in S:
                continue
            S2 = S + [i]
            if not is_feasible_set(S2, incompat_idx, B, OC, gamma):
                continue

            new_obj = obj_value(S2, b_lin, w_pair_obj)
            new_cost = total_cost(S2, OC, gamma)

            marg_gain = new_obj - base_obj
            marg_cost = new_cost - base_cost
            score = marg_gain / (marg_cost if marg_cost > 1e-9 else 1.0)

            candidates.append((i, score, marg_gain))

        if not candidates:
            break

        # Prefer positive-marginal candidates if they exist
        pos = [c for c in candidates if c[2] > 1e-12]
        if pos:
            pool = pos
        else:
            # All are non-positive: stop most of the time, but sometimes explore
            if rng.random() > p_explore:
                break
            pool = candidates  # explore a random feasible move

        scores = [c[1] for c in pool]
        s_max, s_min = max(scores), min(scores)
        cutoff = s_max - alpha_rcl * (s_max - s_min)  # standard RCL threshold
        rcl = [c for c in pool if c[1] >= cutoff]

        i_pick = rng.choice(rcl)[0]
        S.append(i_pick)
        base_obj = obj_value(S, b_lin, w_pair_obj)
        base_cost = total_cost(S, OC, gamma)

    return S

# ---- GRASP: local search (best-improving add/remove/swap) ----
def local_search(S, m, incompat_idx, B, OC, gamma, b_lin, w_pair_obj):
    S = sorted(set(S))
    improved = True

    while improved:
        improved = False
        best_move = None
        best_val = obj_value(S, b_lin, w_pair_obj)

        # 1) Try ADD
        for j in range(m):
            if j in S:
                continue
            S2 = S + [j]
            if not is_feasible_set(S2, incompat_idx, B, OC, gamma):
                continue
            v2 = obj_value(S2, b_lin, w_pair_obj)
            if v2 > best_val + 1e-9:
                best_val = v2
                best_move = ("add", j)

        # 2) Try REMOVE
        for i in list(S):
            S2 = [x for x in S if x != i]
            if not is_feasible_set(S2, incompat_idx, B, OC, gamma):
                continue
            v2 = obj_value(S2, b_lin, w_pair_obj)
            if v2 > best_val + 1e-9:
                best_val = v2
                best_move = ("remove", i)

        # 3) Try SWAP
        Sset = set(S)
        for i in list(S):
            for j in range(m):
                if j in Sset:
                    continue
                S2 = [x for x in S if x != i] + [j]
                if not is_feasible_set(S2, incompat_idx, B, OC, gamma):
                    continue
                v2 = obj_value(S2, b_lin, w_pair_obj)
                if v2 > best_val + 1e-9:
                    best_val = v2
                    best_move = ("swap", i, j)

        if best_move is not None:
            improved = True
            if best_move[0] == "add":
                S.append(best_move[1])
            elif best_move[0] == "remove":
                S = [x for x in S if x != best_move[1]]
            else:  # swap
                _, i, j = best_move
                S = [x for x in S if x != i] + [j]
            S = sorted(set(S))

    return S

def run_grasp(m, incompat_idx, B, OC, gamma, b_lin, w_pair_obj,
             iters=200, alpha_rcl=0.3, p_explore=0.10, seed=42):
    rng = random.Random(seed)
    best_S, best_v = [], float("-inf")

    for _ in range(iters):
        S0 = grasp_construct(
            m, incompat_idx, B, OC, gamma, b_lin, w_pair_obj,
            alpha_rcl=alpha_rcl, p_explore=p_explore, rng=rng
        )
        S1 = local_search(S0, m, incompat_idx, B, OC, gamma, b_lin, w_pair_obj)
        v1 = obj_value(S1, b_lin, w_pair_obj)
        if v1 > best_v:
            best_S, best_v = S1, v1

    return best_S, best_v

