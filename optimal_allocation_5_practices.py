def g_carbon(S, CCP, CSP, alpha):
    V = sum(CSP[i] for i in S)
    for a, b in combinations(S, 2):
        jk = (a, b) if a < b else (b, a)
        V += alpha.get(jk, 0.0)
    return CCP * V

def g_yield(S, CROP_PRICE, DY, delta):
    Y = sum(DY[i] for i in S)
    for a, b in combinations(S, 2):
        jk = (a, b) if a < b else (b, a)
        Y += delta.get(jk, 0.0)
    return CROP_PRICE * Y

def g_cost(S, OC, gamma):
    W = sum(OC[i] for i in S)
    for a, b in combinations(S, 2):
        jk = (a, b) if a < b else (b, a)
        W += gamma.get(jk, 0.0)
    return -W  # negative because cost is subtracted

def shapley_generic(bundle, value_fn):
    n = len(bundle)
    fact = [math.factorial(i) for i in range(n+1)]
    denom = fact[n]
    players = list(bundle)
    phi = {i: 0.0 for i in players}

    for i in players:
        others = [p for p in players if p != i]
        for s in range(0, n):
            for S in combinations(others, s):
                S = list(S)
                w = fact[s] * fact[n - s - 1] / denom
                phi[i] += w * (value_fn(S + [i]) - value_fn(S))
    return phi

phi_carbon = shapley_generic(chosen, lambda S: g_carbon(S, CCP, CSP, alpha))
