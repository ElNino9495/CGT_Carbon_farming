# OPTIONAL: Also decompose Shapley into 3 parts (interpretability)
# Carbon revenue part: CCP*V(C)
# Yield revenue part : CROP_PRICE*Y(C)
# Cost part          : -W(C)
# This helps you write Table II style breakdown.
