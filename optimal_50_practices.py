def tri(rng: random.Random, lo: float, hi: float, mode_frac: float) -> float:
    mode = lo + mode_frac * (hi - lo)
    return rng.triangular(lo, hi, mode)

def pair_key(i: int, j: int) -> Tuple[int, int]:
    return (i, j) if i < j else (j, i)


