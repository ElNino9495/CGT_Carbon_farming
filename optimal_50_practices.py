@dataclass
class BundleData:
    ids: List[str]
    cats: List[str]
    ex_groups: List[str]
    CSP: List[float]
    OC: List[float]
    DY: List[float]
    alpha: Dict[Tuple[int,int], float]
    delta: Dict[Tuple[int,int], float]
    gamma: Dict[Tuple[int,int], float]
    incompat_idx: List[Tuple[int,int]]

def generate_data(practices: List[str], seed: int, out_dir: str) -> BundleData:
    rng = random.Random(seed)
    ids = practices[:]
    n = len(ids)

    cats, ex_groups = [], []
    for nm in ids:
        c, ex = classify_practice(nm)
        cats.append(c)
        ex_groups.append(ex)

    CSP, OC, DY = [], [], []
    for i in range(n):
        r = BASE_RANGES.get(cats[i], BASE_RANGES["general"])
        csp = tri(rng, *r["CSP"], mode_frac=0.62)
        dy  = tri(rng, *r["DY"],  mode_frac=0.55)
        oc  = tri(rng, *r["OC"],  mode_frac=0.58)

        s = ids[i].lower()

        # Practical tweaks for realism
        if cats[i] == "water":
            # AWD/drainage can slightly reduce yield if poorly managed
            if any(k in s for k in ["drain", "awd", "aerobic"]):
                dy += rng.uniform(-50, 30)
            # Laser leveling cost higher
            if "laser" in s:
                oc += rng.uniform(1200, 2200)
                dy += rng.uniform(20, 80)

        if cats[i] == "nitrogen":
            # Inhibitors / CRU / UDP cost more, but yield benefit modest
            if any(k in s for k in ["inhibitor", "controlled-release", "cru", "udp", "deep placement"]):
                oc += rng.uniform(900, 2200)
                dy += rng.uniform(0, 40)
            # LCC/SSNM/precision: moderate cost, good efficiency
            if any(k in s for k in ["lcc", "ssnm", "precision"]):
                dy += rng.uniform(10, 60)

        if cats[i] == "residue":
            # Avoid burning / removal: cost moderate; yield depends
            if "no open-field burning" in s:
                oc += rng.uniform(200, 800)
            if "incorporation" in s:
                dy += rng.uniform(-40, 40)  # could hurt or help
            if "compost" in s:
                dy += rng.uniform(10, 60)

        if cats[i] == "soil":
            if "biochar" in s:
                oc += rng.uniform(1500, 3500)
                csp += rng.uniform(0.3, 0.9)
                dy += rng.uniform(0, 60)
            if "cover crop" in s or "green manuring" in s:
                oc += rng.uniform(500, 1400)
                csp += rng.uniform(0.2, 0.6)

        # Clamp
        csp = max(0.02, min(3.5, csp))
        dy  = max(-400.0, min(600.0, dy))
        oc  = max(150.0, min(9000.0, oc))

        CSP.append(round(csp, 4))
        DY.append(round(dy, 2))
        OC.append(round(oc, 2))

    # Complementarity map (to bias interactions)
    complementary = {
        ("water", "nitrogen"), ("water", "residue"), ("water", "soil"),
        ("nitrogen", "soil"), ("residue", "soil"), ("establishment", "water"),
        ("establishment", "nitrogen"), ("establishment", "water"),
    }
    def is_comp(a: str, b: str) -> bool:
        return (a, b) in complementary or (b, a) in complementary

    alpha: Dict[Tuple[int,int], float] = {}
    delta: Dict[Tuple[int,int], float] = {}
    gamma: Dict[Tuple[int,int], float] = {}

    # Dense interactions: all pairs
    for i, j in combinations(range(n), 2):
        same = (cats[i] == cats[j])
        comp = is_comp(cats[i], cats[j])

        # Mixed-sign core driver
        mu = (0.30 if comp else 0.0) + (-0.22 if same else 0.0)
        z1 = rng.gauss(mu, 0.50)   # for alpha
        z2 = rng.gauss(mu, 0.70)   # for delta
        z3 = rng.gauss((0.18 if same else 0.0) + (-0.08 if comp else 0.0), 0.38)  # for gamma

        # alpha: tCO2e interaction ~ up to 25% of min CSP
        a = (0.25 * min(CSP[i], CSP[j])) * z1
        a = max(-0.80, min(1.00, a))
        alpha[(i, j)] = round(a, 4)

        # delta: kg/ha interaction bounded
        base_y = min(max(abs(DY[i]), 70.0), max(abs(DY[j]), 70.0), 280.0)
        d = (0.25 * base_y) * z2
        d = max(-220.0, min(260.0, d))
        delta[(i, j)] = round(d, 2)

        # gamma: INR/ha interaction bounded (coordination cost or saving)
        g = (0.20 * min(OC[i], OC[j])) * z3
        g = max(-1000.0, min(2200.0, g))
        gamma[(i, j)] = round(g, 2)

    # Incompatibilities:
    incompat: Set[Tuple[int,int]] = set()

    # A) Exclusive groups (all pairs within group are incompatible)
    group_to_items: Dict[str, List[int]] = {}
    for idx, g in enumerate(ex_groups):
        if g:
            group_to_items.setdefault(g, []).append(idx)
    for g, items in group_to_items.items():
        if len(items) >= 2:
            for i, j in combinations(items, 2):
                incompat.add(pair_key(i, j))

    # B) Add a few extra "hard-conflict" pairs (small % of pairs)
    # Score: very negative alpha/delta or very large positive gamma
    all_pairs = list(combinations(range(n), 2))
    def conflict_score(i: int, j: int) -> float:
        a = alpha[(i, j)]
        d = delta[(i, j)] / 200.0
        g = gamma[(i, j)] / 1500.0
        return a + d - g

    ranked = sorted(all_pairs, key=lambda p: conflict_score(p[0], p[1]))
    target_extra = int(0.05 * len(all_pairs))  # ~5% extra conflicts beyond exclusives
    added = 0
    for (i, j) in ranked:
        if added >= target_extra:
            break
        if (i, j) in incompat:
            continue
        if conflict_score(i, j) < -0.60:
            incompat.add((i, j))
            added += 1

    incompat_idx = sorted(incompat)

    # Export CSVs
    os.makedirs(out_dir, exist_ok=True)

    df_pr = pd.DataFrame({
        "idx": list(range(n)),
        "practice": ids,
        "category": cats,
        "exclusive_group": ex_groups,
        "CSP_tCO2e": CSP,
        "OC_INR": OC,
        "dY_kg_per_ha": DY,
    })
    df_pr.to_csv(os.path.join(out_dir, "practices.csv"), index=False)

    df_int = pd.DataFrame([
        {"i": i, "j": j, "alpha": alpha[(i, j)], "delta": delta[(i, j)], "gamma": gamma[(i, j)]}
        for (i, j) in all_pairs
    ])
    df_int.to_csv(os.path.join(out_dir, "interactions.csv"), index=False)

    df_inc = pd.DataFrame([{"i": i, "j": j} for (i, j) in incompat_idx])
    df_inc.to_csv(os.path.join(out_dir, "incompatibilities.csv"), index=False)

    print(f"[OK] Wrote: {out_dir}/practices.csv, interactions.csv, incompatibilities.csv")
    print(f"n={n}, pairs={len(all_pairs)}, incompatibilities={len(incompat_idx)}")
    print("CSP range:", (min(CSP), max(CSP)), "tCO2e")
    print("DY  range:", (min(DY), max(DY)), "kg/ha")
    print("OC  range:", (min(OC), max(OC)), "INR")

    return BundleData(
        ids=ids, cats=cats, ex_groups=ex_groups,
        CSP=CSP, OC=OC, DY=DY,
        alpha=alpha, delta=delta, gamma=gamma,
        incompat_idx=incompat_idx
    )


# ============================================================
# 3) Build objective coefficients for your bundle model
#    Objective: sum b_i x_i + sum w_ij x_i x_j
# ============================================================
def build_objective(data: BundleData, CCP: float, CROP_PRICE: float):
    n = len(data.ids)
    b = [CCP * data.CSP[i] + CROP_PRICE * data.DY[i] - data.OC[i] for i in range(n)]

    # Convert alpha/delta/gamma into one quadratic objective coefficient w_ij
    w = [[0.0] * n for _ in range(n)]
    gmat = [[0.0] * n for _ in range(n)]
    for (i, j), a in data.alpha.items():
        d = data.delta[(i, j)]
        g = data.gamma[(i, j)]
        wij = CCP * a + CROP_PRICE * d - g
        w[i][j] = w[j][i] = float(wij)
        gmat[i][j] = gmat[j][i] = float(g)
    return b, w, gmat


# ============================================================
# 4) GRASP (fast heuristic) using incremental updates
# ============================================================
def build_incompat_sets(n: int, incompat_idx: List[Tuple[int,int]]) -> List[Set[int]]:
    inc = [set() for _ in range(n)]
    for i, j in incompat_idx:
        inc[i].add(j)
        inc[j].add(i)
    return inc

def grasp_solve(n: int, b: List[float], w: List[List[float]], OC: List[float], gmat: List[List[float]],
                incompat: List[Set[int]], B: float,
                iters: int = 200, alpha_rcl: float = 0.3, p_explore: float = 0.10, seed: int = 0):
    rng = random.Random(seed)

    def construct_once():
        inS = [False]*n
        S = []
        sumW = [0.0]*n  # sum of w[i][j] over j in S
        sumG = [0.0]*n  # sum of gamma[i][j] over j in S
        cur_val = 0.0
        cur_cost = 0.0

        while True:
            cand = []
            for i in range(n):
                if inS[i]:
                    continue
                # incompat check
                if any(inS[j] for j in incompat[i]):
                    continue
                add_cost = OC[i] + sumG[i]
                if cur_cost + add_cost > B + 1e-9:
                    continue
                add_gain = b[i] + sumW[i]
                score = add_gain / max(add_cost, 1.0)
                cand.append((i, score, add_gain, add_cost))

            if not cand:
                break

            pos = [c for c in cand if c[2] > 1e-9]
            if pos:
                pool = pos
            else:
                if rng.random() > p_explore:
                    break
                pool = cand

            scores = [c[1] for c in pool]
            smax, smin = max(scores), min(scores)
            cutoff = smax - alpha_rcl*(smax - smin)
            rcl = [c for c in pool if c[1] >= cutoff]
            i = rng.choice(rcl)[0]

            # apply add
            inS[i] = True
            S.append(i)
            cur_val += b[i] + sumW[i]
            cur_cost += OC[i] + sumG[i]
            for k in range(n):
                if k == i:
                    continue
                sumW[k] += w[k][i]
                sumG[k] += gmat[k][i]
        return S

    def local_search(S0: List[int]):
        inS = [False]*n
        for i in S0:
            inS[i] = True

        # build incremental sums
        sumW = [0.0]*n
        sumG = [0.0]*n
        cur_val = 0.0
        cur_cost = 0.0

        for i in range(n):
            if inS[i]:
                cur_val += b[i]
                cur_cost += OC[i]
        for i, j in combinations(range(n), 2):
            if inS[i] and inS[j]:
                cur_val += w[i][j]
                cur_cost += gmat[i][j]
        for i in range(n):
            if not inS[i]:
                # sum over chosen
                sw = 0.0
                sg = 0.0
                for j in range(n):
                    if inS[j]:
                        sw += w[i][j]
                        sg += gmat[i][j]
                sumW[i] = sw
                sumG[i] = sg
            else:
                # sum to other chosen
                sw = 0.0
                sg = 0.0
                for j in range(n):
                    if inS[j] and j != i:
                        sw += w[i][j]
                        sg += gmat[i][j]
                sumW[i] = sw
                sumG[i] = sg

        def feasible_add(x: int, inS_local: List[bool], cost_now: float) -> bool:
            if inS_local[x]:
                return False
            if any(inS_local[j] for j in incompat[x]):
                return False
            return cost_now + (OC[x] + sumG[x]) <= B + 1e-9

        improved = True
        while improved:
            improved = False
            best_gain = 0.0
            best_move = None

            # ADD moves
            for j in range(n):
                if inS[j]:
                    continue
                if not feasible_add(j, inS, cur_cost):
                    continue
                gain = b[j] + sumW[j]
                if gain > best_gain + 1e-9:
                    best_gain = gain
                    best_move = ("add", j)

            # REMOVE moves (useful when item net contribution becomes negative due to interactions)
            for i in range(n):
                if not inS[i]:
                    continue
                gain = -(b[i] + sumW[i])  # removing increases objective if (b+sumW)<0
                if gain > best_gain + 1e-9:
                    best_gain = gain
                    best_move = ("rem", i)

            # SWAP moves
            for i in range(n):
                if not inS[i]:
                    continue
                # after removing i, cost/compat change
                cost_after_rem = cur_cost - (OC[i] + sumG[i])
                for j in range(n):
                    if inS[j]:
                        continue
                    # incompat check against S\{i}
                    if any(inS[k] for k in incompat[j] if k != i):
                        continue
                    add_cost_after = OC[j] + (sumG[j] - gmat[j][i])
                    if cost_after_rem + add_cost_after > B + 1e-9:
                        continue
                    # add gain after removal
                    add_gain_after = b[j] + (sumW[j] - w[j][i])
                    rem_gain = -(b[i] + sumW[i])
                    gain = rem_gain + add_gain_after
                    if gain > best_gain + 1e-9:
                        best_gain = gain
                        best_move = ("swap", i, j)

            if best_move is None:
                break

            improved = True
            typ = best_move[0]

            def apply_remove(i: int):
                nonlocal cur_val, cur_cost
                inS[i] = False
                cur_val -= b[i] + sumW[i]
                cur_cost -= OC[i] + sumG[i]
                for k in range(n):
                    if k == i:
                        continue
                    sumW[k] -= w[k][i]
                    sumG[k] -= gmat[k][i]
                sumW[i] = 0.0
                sumG[i] = 0.0

            def apply_add(j: int):
                nonlocal cur_val, cur_cost
                inS[j] = True
                cur_val += b[j] + sumW[j]
                cur_cost += OC[j] + sumG[j]
                for k in range(n):
                    if k == j:
                        continue
                    sumW[k] += w[k][j]
                    sumG[k] += gmat[k][j]
                # recompute sumW[j],sumG[j] to chosen (excluding itself)
                sw = 0.0
                sg = 0.0
                for k in range(n):
                    if inS[k] and k != j:
                        sw += w[j][k]
                        sg += gmat[j][k]
                sumW[j] = sw
                sumG[j] = sg

            if typ == "add":
                apply_add(best_move[1])
            elif typ == "rem":
                apply_remove(best_move[1])
            else:
                i, j = best_move[1], best_move[2]
                apply_remove(i)
                apply_add(j)

        S_final = [i for i in range(n) if inS[i]]
        return S_final, cur_val, cur_cost

    best_S, best_val, best_cost = [], float("-inf"), 0.0
    for t in range(iters):
        S0 = construct_once()
        S1, v1, c1 = local_search(S0)
        if v1 > best_val:
            best_S, best_val, best_cost = S1, v1, c1

    return best_S, best_val, best_cost


# ============================================================
# 5) MILP (PuLP) warm-started from GRASP (linearization)
# ============================================================
def solve_milp(n: int, b: List[float], w: List[List[float]], OC: List[float], gmat: List[List[float]],
              incompat_idx: List[Tuple[int,int]], B: float, start_S: List[int] | None,
              time_limit: int | None = None, warmstart: bool = True):
    try:
        import pulp
    except ImportError:
        import sys, subprocess
        subprocess.check_call([sys.executable, "-m", "pip", "install", "pulp"])
        import pulp

    prob = pulp.LpProblem("QKP_Bundle", pulp.LpMaximize)

    x = {i: pulp.LpVariable(f"x_{i}", cat="Binary") for i in range(n)}
    pairs = [(i, j) for i in range(n) for j in range(i+1, n)]
    y = {(i, j): pulp.LpVariable(f"y_{i}_{j}", cat="Binary") for (i, j) in pairs}

    # objective
    prob += pulp.lpSum(b[i] * x[i] for i in range(n)) + pulp.lpSum(w[i][j] * y[(i, j)] for (i, j) in pairs)

    # incompatibilities
    for (i, j) in incompat_idx:
        prob += x[i] + x[j] <= 1

    # linearize y_ij = x_i * x_j
    for (i, j) in pairs:
        prob += y[(i, j)] <= x[i]
        prob += y[(i, j)] <= x[j]
        prob += y[(i, j)] >= x[i] + x[j] - 1

    # budget
    prob += pulp.lpSum(OC[i] * x[i] for i in range(n)) + pulp.lpSum(gmat[i][j] * y[(i, j)] for (i, j) in pairs) <= B

    # warm start
    if warmstart and start_S is not None:
        start_set = set(start_S)
        for i in range(n):
            x[i].setInitialValue(1 if i in start_set else 0)
        for (i, j) in pairs:
            y[(i, j)].setInitialValue(1 if (i in start_set and j in start_set) else 0)

    # solve
    # CBC supports timeLimit; warmStart might not always be available in some builds
    solver_kwargs = {"msg": False}
    if time_limit is not None:
        solver_kwargs["timeLimit"] = time_limit
    if warmstart:
        solver_kwargs["warmStart"] = True

    solver = pulp.PULP_CBC_CMD(**solver_kwargs)
    prob.solve(solver)

    status = pulp.LpStatus[prob.status]
    chosen = [i for i in range(n) if int(pulp.value(x[i])) == 1]
    obj = float(pulp.value(prob.objective))
    return status, chosen, obj


