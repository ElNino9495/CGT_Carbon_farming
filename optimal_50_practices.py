def classify_practice(name: str) -> Tuple[str, str]:
    s = name.lower()

    # Categories (broad)
    if any(k in s for k in ["awd", "drain", "irrig", "flood", "bund", "laser", "raised beds", "furrow", "aerobic", "channel"]):
        cat = "water"
    elif any(k in s for k in ["direct-seeded", "dsr", "dry seeding", "puddling", "strip till", "reduced till", "controlled traffic", "mechanized", "weeding"]):
        cat = "establishment"
    elif any(k in s for k in ["n rate", "split", "urea", "udp", "cru", "inhibitor", "ssnm", "lcc", "fertiliz", "manure", "legume", "precision fertilizer"]):
        cat = "nitrogen"
    elif any(k in s for k in ["straw", "burn", "mulch", "composting", "bioenergy", "baling", "incorporation"]):
        cat = "residue"
    elif any(k in s for k in ["biochar", "farmyard manure", "green manuring", "cover crop", "soc", "stabilized organic"]):
        cat = "soil"
    else:
        cat = "general"

    # Exclusivity groups (only where it makes sense)
    ex = ""

    # AWD variants: treat as alternative "ways of doing AWD"
    if "awd" in s:
        ex = "awd_variant"

    # Establishment method: you typically pick one primary establishment style
    if any(k in s for k in ["direct-seeded", "dsr", "dry seeding", "reduced puddling", "strip till", "reduced till",
                            "shift from transplanted", "transplanted puddled", "non-puddled"]):
        ex = "establishment_method"

    # Aerobic/raised-bed regime (distinct from AWD-style flooding management)
    if any(k in s for k in ["aerobic rice", "raised beds", "furrow-irrigated"]):
        ex = "aerobic_regime"

    # Duration choice: long-duration -> shorter OR short-duration varieties (treat as alternatives)
    if "short-duration" in s or "long-duration" in s:
        ex = "duration_choice"

    # Residue fate: picking one main straw management pathway (simplification)
    # (baling is an enabler, but we keep it under the same group; you can relax this if you want)
    if "straw" in s or "burn" in s or "bioenergy" in s or "baling" in s:
        ex = "residue_fate"

    return cat, ex


