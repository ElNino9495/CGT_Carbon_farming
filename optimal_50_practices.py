# ---- helpers to compute cost/objective for any chosen set S ----
def calc_cost(S, OC, gmat):
    S = sorted(S)
    cost = sum(OC[i] for i in S)
    for a, b in combinations(S, 2):
        cost += gmat[a][b]
    return float(cost)

def calc_obj(S, b, w_obj):
    S = sorted(S)
    val = sum(b[i] for i in S)
    for a, c in combinations(S, 2):
        val += w_obj[a][c]
    return float(val)

